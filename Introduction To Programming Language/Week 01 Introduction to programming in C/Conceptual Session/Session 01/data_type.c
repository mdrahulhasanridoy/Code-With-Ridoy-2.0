//Description: This program is to show the data type of C language

#include<stdio.h>
int main()
{
    int x = 5;
    float y = 42.524;
    
    printf("%d\n",x);
    printf("%f",y);
    
    return 0;
}
/*Explanation: Here we have declared two variables x and y. 
x is an integer variable and y is a float variable.
We have assigned the value 5 to x and 42.524 to y.
Then we have printed the value of x and y. The output will be 5 and 42.524 respectively.*/