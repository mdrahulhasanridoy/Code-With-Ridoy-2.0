//Description : Write a program that mixed integer and character input.
#include<stdio.h>
int main()
{
    int a;
    char c;

    scanf("%d %c",&a,&c);

    printf("%d %c",a,c);

    return 0;
}

//Explanation: Here we have declared two variables a and c.
//Then we have scanned the value of a and c.
//Then we have printed the value of a and c.
//The output will be the value of a and c.