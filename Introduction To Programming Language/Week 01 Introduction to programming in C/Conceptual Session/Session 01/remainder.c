//Description: Write a program that prints the remainder of two integers.

#int main()
{
    int number = 10;

    int remainder = number % 2;

    printf("The remainder is %d",remainder);

    return 0;
}

//Explanation: Here we have declared a variable number and assigned the value 10 to it.
//Then we have declared a variable remainder and assigned the value of number % 2 to it.
//Then we have printed the remainder.
//The output will be the remainder of 10 divided by 2.